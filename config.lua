-- ============================================================
-- 各ポケットへのボールの到達確率（二項分布に準拠した重み）
-- 列数 n のとき、ポケット k（0〜n）への重みは C(n,k) に比例
-- ============================================================

-- 二項係数を計算するヘルパー関数
local function binomialCoeff(n, k)
    if k == 0 or k == n then return 1 end
    if k > n then return 0 end
    local result = 1
    for i = 0, k - 1 do
        result = result * (n - i) / (i + 1)
    end
    return math.floor(result + 0.5)
end

-- Config.Weights[rows] = {w0, w1, ..., wN}（累積重みテーブル）
Config.Weights = {}
for _, rows in ipairs(Config.ValidRows) do
    local weights = {}
    local total   = 0
    for k = 0, rows do
        local w = binomialCoeff(rows, k)
        total = total + w
        weights[k + 1] = total  -- 1-indexed 累積重み
    end
    Config.Weights[rows] = { cumulative = weights, total = total }
end
